def reverseString(myString):
    s = Stack()
    output = ""
    for lettre in myString:
        s.push(lettre)
    while not s.isEmpty():
        output = output + s.pop()
    return output

def parChecker(symbolString):
    s = Stack()
    balanced = True
    index = 0
    while index < len(symbolString) and balanced:
        symbol = symbolString[index]
        if symbol == "(":
            s.push(symbol)
        else:
            if s.isEmpty():
                balanced = False
            else:
                s.pop()

        index = index + 1

    if balanced and s.isEmpty():
        return True
    else:
        return False

